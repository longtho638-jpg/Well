import fs from 'fs';

const FILES = ['src/locales/vi.ts', 'src/locales/en.ts'];
const MARKER = '// --- AUTO-GENERATED BY WOW FIXER ---';

FILES.forEach(file => {
    if (!fs.existsSync(file)) return;
    
    let content = fs.readFileSync(file, 'utf-8');
    const index = content.indexOf(MARKER);
    
    if (index !== -1) {
        // Find the "};" at the very end of the file or just strip from MARKER to the second to last line?
        // The previous script inserted BEFORE the last "};". 
        // So we should remove everything from MARKER up to the last "};" (exclusive) and restore stricture?
        // Actually, simpler: The MARKER was the start of the bad block. 
        // The bad block *added* new keys. We just need to remove them.
        
                // Strategy: Truncate file at MARKER
        
                const cleanContent = content.substring(0, index);
        
                // We assume the file needs to be closed with "};"
        
                fs.writeFileSync(file, cleanContent + '\n};\n');
        
                console.log(`✅ Repaired ${file}`);
        
        
    } else {
        console.log(`ℹ️ Marker not found in ${file}`);
    }
});
